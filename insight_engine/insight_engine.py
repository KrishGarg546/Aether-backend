def load_receipts(path: str | None = None) -> pd.DataFrame:
    """Load communication receipt records from CSV.

    This is the sole data-ingestion entry point for the Insights Engine.
    It resolves the storage path, validates the schema, and returns a clean
    DataFrame ready for metric calculation.

    If the receipts file does not yet exist — for example, during early
    development or in a fresh environment — the function returns an empty
    DataFrame with the correct column schema so all downstream callers can
    proceed without branching on file existence.

    Parameters
    ----------
    path:
        Absolute or relative path to the receipts CSV.  When ``None``,
        the function first checks the ``AETHER_RECEIPTS_PATH`` environment
        variable and then falls back to ``DEFAULT_RECEIPTS_PATH``.

    Returns
    -------
    pd.DataFrame
        DataFrame containing all historical receipt records with columns
        ``receipt_id``, ``communication_id``, ``campaign_id``, ``event``,
        and ``event_timestamp``.  Returns an empty DataFrame with the same
        schema if the file does not exist.  All columns are string-typed
        for consistency with the Receipt API's storage contract.

    Raises
    ------
    ValueError
        If the file exists but is missing one or more required columns.
        The error message enumerates every missing column name.
    """
    resolved_path: str = (
        path
        or os.environ.get("AETHER_RECEIPTS_PATH", "")
        or DEFAULT_RECEIPTS_PATH
    )

    if not os.path.isfile(resolved_path):
        return pd.DataFrame(columns=RECEIPT_COLUMNS)

    df: pd.DataFrame = pd.read_csv(resolved_path, dtype=str)

    # Strip surrounding whitespace from every string cell to guard against
    # CSV editors or operating systems that introduce invisible characters.
    df = df.apply(lambda col: col.str.strip() if col.dtype == object else col)

    missing_cols: set[str] = set(RECEIPT_COLUMNS) - set(df.columns)
    if missing_cols:
        raise ValueError(
            f"[load_receipts] Receipts CSV at '{resolved_path}' is missing "
            f"required column(s): {sorted(missing_cols)}.  "
            f"Expected: {RECEIPT_COLUMNS}"
        )

    return df[RECEIPT_COLUMNS]


