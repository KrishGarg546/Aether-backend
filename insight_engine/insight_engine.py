def calculate_channel_metrics(receipts: pd.DataFrame, communications: pd.DataFrame | None = None) -> pd.DataFrame:
    """Calculate per-channel performance metrics from receipt records.

    Groups receipt events by inferred channel and computes the same metric
    set as ``calculate_campaign_metrics()`` for each channel independently.
    Channel is inferred from the ``communication_id`` prefix using the
    convention documented in the module docstring.

    Parameters
    ----------
    receipts:
        DataFrame of receipt records as returned by ``load_receipts()``.
        An empty DataFrame is accepted and produces an empty output
        DataFrame with the correct column schema.
    communications:
        Optional communications DataFrame containing at minimum the
        ``communication_id`` and ``channel`` columns. When provided,
        channel values are resolved directly from this DataFrame rather
        than inferred from communication_id prefixes.

    Returns
    -------
    pd.DataFrame
        One row per inferred channel, with columns:

        ``channel`` (str)
            Channel name (EMAIL, SMS, PUSH, WHATSAPP, or UNKNOWN).
        ``communications`` (int)
            Number of unique communication IDs dispatched on this channel.
        ``delivery_rate`` (float)
            Percentage of dispatched communications delivered on this channel.
        ``open_rate`` (float)
            Percentage of delivered communications opened or read per channel.
        ``click_rate`` (float)
            Percentage of delivered communications clicked per channel.
        ``failure_rate`` (float)
            Percentage of dispatched communications that failed per channel.

        The DataFrame is sorted by ``delivery_rate`` descending so the
        strongest-performing channel appears first.

    Notes
    -----
    - Channels with zero dispatched events produce 0.0 for all rate columns.
    - When a communications DataFrame is provided, explicit channel mappings take precedence over prefix inference.
    - An ``UNKNOWN`` channel row is included when communication_ids cannot be resolved through either mechanism, preserving full receipt coverage in the output.
    """
    if receipts.empty:
        return pd.DataFrame(columns=CHANNEL_METRIC_COLUMNS)

    receipts_with_channel: pd.DataFrame = receipts.copy()

    if communications is not None:
        required_cols: set[str] = {"communication_id", "channel"}
        missing_cols: set[str] = required_cols - set(communications.columns)

        if missing_cols:
            raise ValueError(
                "[calculate_channel_metrics] Communications DataFrame is "
                f"missing required columns: {sorted(missing_cols)}"
            )

        channel_lookup: dict[str, str] = (
            communications[["communication_id", "channel"]]
            .drop_duplicates(subset=["communication_id"])
            .assign(channel=lambda df: df["channel"].astype(str).str.strip().str.upper())
            .set_index("communication_id")["channel"]
            .to_dict()
        )

        receipts_with_channel["channel"] = receipts_with_channel[
            "communication_id"
        ].map(channel_lookup)

        receipts_with_channel["channel"] = receipts_with_channel[
            "channel"
        ].fillna(
            receipts_with_channel["communication_id"].apply(_infer_channel)
        )
    else:
        receipts_with_channel["channel"] = receipts_with_channel[
            "communication_id"
        ].apply(_infer_channel)

    channel_rows: list[dict[str, Any]] = []

    for channel, group in receipts_with_channel.groupby("channel", sort=False):
        event_counts: pd.Series = group["event"].value_counts()

        dispatched: int = int(event_counts.get("DISPATCHED", 0))
        delivered:  int = int(event_counts.get("DELIVERED",  0))
        failed:     int = int(event_counts.get("FAILED",     0))
        opened:     int = int(event_counts.get("OPENED",     0))
        read:       int = int(event_counts.get("READ",       0))
        clicked:    int = int(event_counts.get("CLICKED",    0))

        communications: int = group["communication_id"].nunique()

        channel_rows.append(
            {
                "channel":       channel,
                "communications": communications,
                "delivery_rate":  _safe_rate(delivered, dispatched),
                "open_rate":      _safe_rate(opened + read, delivered),
                "click_rate":     _safe_rate(clicked, delivered),
                "failure_rate":   _safe_rate(failed, dispatched),
            }
        )

    result: pd.DataFrame = (
        pd.DataFrame(channel_rows, columns=CHANNEL_METRIC_COLUMNS)
        .sort_values("delivery_rate", ascending=False)
        .reset_index(drop=True)
    )

    return result


