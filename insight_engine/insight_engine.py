"""
insight_engine/insight_engine.py
==================================
Aether CRM – Insights Engine
------------------------------
Responsible for one thing: consume the append-only communication receipt log
produced by the Receipt API, compute deterministic campaign and channel
performance metrics, generate rule-based business recommendations, and surface
human-readable summaries for marketers and downstream systems.

This module sits at the terminal stage of the Aether CRM execution pipeline.
It does NOT send messages, simulate delivery, generate communications, select
audiences, or modify any receipt record.  Those responsibilities belong
exclusively to their respective upstream modules.

Pipeline position
-----------------
Goal Parser
  ↓
Audience Selector
  ↓
Campaign Planner
  ↓
Communication Manager
  ↓
Channel Service
  ↓
Receipt API
  ↓
Insights Engine  ← THIS MODULE

Read-only guarantee
--------------------
The Insights Engine never writes to, reorders, or deletes receipt records.
All receipt data is treated as immutable input consumed from the append-only
event log maintained by the Receipt API.  Any attempt to modify receipt
storage from within this module is a contract violation.

Determinism guarantee
---------------------
All metrics and recommendations are derived exclusively from the receipt CSV
via deterministic arithmetic.  Given identical input data, the Insights Engine
always produces identical outputs regardless of when or where this module runs.

Metric definitions
------------------
The following formulae govern all campaign-level and channel-level metric
calculations.  These definitions are immutable in V1 and must not be changed
without a corresponding ProjectDecision.md entry.

    delivery_rate  = DELIVERED  / DISPATCHED  × 100
    open_rate      = (OPENED + READ) / DELIVERED × 100
    click_rate     = CLICKED    / DELIVERED    × 100
    failure_rate   = FAILED     / DISPATCHED   × 100

Division-by-zero is handled gracefully: any rate whose denominator is zero
is reported as 0.0 rather than raising an exception.

Channel inference
-----------------
Receipt records produced by the Receipt API do not include a channel column.
The Insights Engine infers channel from the communication_id prefix pattern
established by the Channel Service smoke-test convention and the Communication
Manager's naming contract:

    communication_id prefix   →  channel
    ─────────────────────────────────────
    COMM-CH-EMAIL-*           →  EMAIL
    COMM-CH-SMS-*             →  SMS
    COMM-CH-PUSH-*            →  PUSH
    COMM-CH-WA-*              →  WHATSAPP
    (no match)                →  UNKNOWN

For receipt logs generated by the full pipeline (Communication Manager →
Channel Service), the communication_id may carry a different prefix.  In those
cases the engine falls back gracefully to UNKNOWN rather than raising an error.
The Insights Engine also supports an optional communications DataFrame containing communication_id and channel columns. When supplied, this mapping takes precedence over prefix-based inference. Prefix inference remains the fallback mechanism for standalone execution and smoke tests.

Recommendation thresholds (V1, LOCKED)
---------------------------------------
    delivery_rate  < 80 %  →  Investigate channel delivery issues.
    delivery_rate  ≥ 90 %  →  Delivery performance is healthy.
    open_rate      < 20 %  →  Consider revising messaging content or subject lines.
    open_rate      ≥ 40 %  →  Audience engagement is strong; content is resonating.
    click_rate     < 5  %  →  Call-to-action copy or offer may need improvement.
    click_rate     > 15 %  →  Campaign engagement is strong; consider scaling.
    failure_rate   > 20 %  →  High failure rates detected. Review channel strategy.
    failure_rate   > 10 %  →  Elevated failure rate. Monitor channel health closely.

Django integration notes
------------------------
All public functions return either primitive Python types (dict, list) or
pandas DataFrames.  DataFrames can be converted to Django model querysets via
``DataFrame.to_dict(orient="records")`` without modification.  No Django
imports are present; the module is framework-agnostic by design.

Usage
-----
Run as a standalone script (demo / smoke-test mode):

    python insight_engine/insight_engine.py

Or import and call programmatically:

    from insight_engine.insight_engine import (
        load_receipts,
        calculate_campaign_metrics,
        calculate_channel_metrics,
        generate_recommendations,
        print_summary,
    )

    receipts     = load_receipts("path/to/communication_receipts.csv")
    camp_metrics = calculate_campaign_metrics(receipts)
    chan_metrics = calculate_channel_metrics(receipts)
    recs         = generate_recommendations(camp_metrics)
    print_summary(camp_metrics, chan_metrics, recs)
"""

from __future__ import annotations

import os
import sys
from typing import Any

import pandas as pd

# ---------------------------------------------------------------------------
# Constants
# ---------------------------------------------------------------------------

# Default path to the receipt log, consistent with Receipt API convention.
# Override via the AETHER_RECEIPTS_PATH environment variable.
DEFAULT_RECEIPTS_PATH: str = os.path.join(
    os.path.dirname(os.path.dirname(os.path.abspath(__file__))),
    "data_generation",
    "data",
    "communication_receipts.csv",
)

# Canonical column schema expected in every receipts DataFrame consumed by
# this module.  This mirrors RECEIPT_COLUMNS in receipt_api.py and must
# remain in sync with the Receipt API contract.
RECEIPT_COLUMNS: list[str] = [
    "receipt_id",
    "communication_id",
    "campaign_id",
    "event",
    "event_timestamp",
]

# Columns emitted by calculate_channel_metrics().  Listed explicitly so
# downstream consumers can rely on a stable schema contract.
CHANNEL_METRIC_COLUMNS: list[str] = [
    "channel",
    "communications",
    "delivery_rate",
    "open_rate",
    "click_rate",
    "failure_rate",
]

# Complete set of supported lifecycle events (mirrors Receipt API).
VALID_EVENTS: set[str] = {
    "DISPATCHED",
    "DELIVERED",
    "FAILED",
    "OPENED",
    "READ",
    "CLICKED",
}

# ---------------------------------------------------------------------------
# Recommendation thresholds (V1, LOCKED)
# Changing these values requires a corresponding ProjectDecision.md entry.
# ---------------------------------------------------------------------------

_DELIVERY_RATE_CRITICAL_THRESHOLD: float  = 80.0   # Below this → critical alert.
_DELIVERY_RATE_HEALTHY_THRESHOLD:  float  = 90.0   # Above this → positive signal.
_OPEN_RATE_LOW_THRESHOLD:          float  = 20.0   # Below this → content review.
_OPEN_RATE_HIGH_THRESHOLD:         float  = 40.0   # Above this → positive signal.
_CLICK_RATE_LOW_THRESHOLD:         float  =  5.0   # Below this → CTA review.
_CLICK_RATE_HIGH_THRESHOLD:        float  = 15.0   # Above this → scale campaign.
_FAILURE_RATE_CRITICAL_THRESHOLD:  float  = 20.0   # Above this → urgent review.
_FAILURE_RATE_ELEVATED_THRESHOLD:  float  = 10.0   # Above this → monitor closely.

# ---------------------------------------------------------------------------
# Channel inference map
# Derived from the communication_id naming conventions established across
# the Communication Manager and Channel Service smoke tests.
# ---------------------------------------------------------------------------

_CHANNEL_PREFIX_MAP: dict[str, str] = {
    "COMM-CH-EMAIL-": "EMAIL",
    "COMM-CH-SMS-":   "SMS",
    "COMM-CH-PUSH-":  "PUSH",
    "COMM-CH-WA-":    "WHATSAPP",
}

_UNKNOWN_CHANNEL: str = "UNKNOWN"


# ---------------------------------------------------------------------------
# Internal helpers
# ---------------------------------------------------------------------------


def _safe_rate(numerator: float, denominator: float) -> float:
    """Compute a percentage rate, returning 0.0 when the denominator is zero.

    This guard prevents ZeroDivisionError across all metric calculations
    and ensures that campaigns or channels with no dispatched communications
    produce a 0.0 rate rather than raising an exception.

    Parameters
    ----------
    numerator:
        The count forming the numerator of the rate expression.
    denominator:
        The count forming the denominator of the rate expression.

    Returns
    -------
    float
        ``(numerator / denominator) * 100`` rounded to two decimal places,
        or ``0.0`` if ``denominator`` is zero.
    """
    if denominator == 0:
        return 0.0
    return round((numerator / denominator) * 100, 2)


def _infer_channel(communication_id: str) -> str:
    """Infer the delivery channel from a communication_id prefix.

    The Channel Service and Communication Manager encode channel information
    in the communication_id using a structured prefix convention.  This
    function decodes that convention deterministically.

    Parameters
    ----------
    communication_id:
        The communication identifier to inspect.

    Returns
    -------
    str
        The inferred channel name (e.g. ``"EMAIL"``), or ``"UNKNOWN"`` if no
        known prefix matches.

    Notes
    -----
    The prefix map is defined in ``_CHANNEL_PREFIX_MAP`` and must be kept in
    sync with the naming conventions used by the Communication Manager and the
    Channel Service smoke test.
    """
    for prefix, channel in _CHANNEL_PREFIX_MAP.items():
        if communication_id.startswith(prefix):
            return channel
    return _UNKNOWN_CHANNEL


