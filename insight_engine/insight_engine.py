def print_summary(
    campaign_metrics: dict[str, Any],
    channel_metrics:  pd.DataFrame,
    recommendations:  list[str],
) -> None:
    """Print a structured, human-readable campaign performance summary.

    Formats and writes the complete insights output to stdout in a style
    consistent with the Receipt API and Channel Service summary functions.
    The output is intentionally terminal-friendly for development and
    smoke-test contexts; Django views should consume the structured return
    values from the calculation functions directly.

    Parameters
    ----------
    campaign_metrics:
        Dictionary as returned by ``calculate_campaign_metrics()``.
    channel_metrics:
        DataFrame as returned by ``calculate_channel_metrics()``.
    recommendations:
        List of recommendation strings as returned by
        ``generate_recommendations()``.

    Returns
    -------
    None
        This function has no return value.  All output is written to stdout.
    """
    bar: str = "─" * 60

    # ── Header ───────────────────────────────────────────────────────────────

    print(f"\n{'=' * 60}")
    print("  AETHER CRM – Insights Engine Report")
    print(f"{'=' * 60}")

    # ── Campaign-level metrics ────────────────────────────────────────────────

    print(f"\n{bar}")
    print("  Campaign Performance Metrics")
    print(bar)
    print(
        f"  {'Total Communications':<30}  "
        f"{campaign_metrics['total_communications']:>8,}"
    )
    print(
        f"  {'Total Receipt Events':<30}  "
        f"{campaign_metrics['total_receipts']:>8,}"
    )
    print(
        f"  {'Delivery Rate':<30}  "
        f"{campaign_metrics['delivery_rate']:>7.1f} %"
    )
    print(
        f"  {'Open Rate':<30}  "
        f"{campaign_metrics['open_rate']:>7.1f} %"
    )
    print(
        f"  {'Click Rate':<30}  "
        f"{campaign_metrics['click_rate']:>7.1f} %"
    )
    print(
        f"  {'Failure Rate':<30}  "
        f"{campaign_metrics['failure_rate']:>7.1f} %"
    )

    # ── Channel-level metrics ─────────────────────────────────────────────────

    print(f"\n{bar}")
    print("  Channel Performance Metrics")
    print(bar)

    if channel_metrics.empty:
        print("  No channel data available.")
    else:
        # Column headers
        print(
            f"  {'Channel':<12}  {'Comms':>6}  {'Delivered':>9}  "
            f"{'Opened':>7}  {'Clicked':>7}  {'Failed':>7}"
        )
        print(f"  {'─' * 12}  {'─' * 6}  {'─' * 9}  {'─' * 7}  {'─' * 7}  {'─' * 7}")

        for _, row in channel_metrics.iterrows():
            print(
                f"  {row['channel']:<12}  "
                f"{int(row['communications']):>6,}  "
                f"{row['delivery_rate']:>8.1f}%  "
                f"{row['open_rate']:>6.1f}%  "
                f"{row['click_rate']:>6.1f}%  "
                f"{row['failure_rate']:>6.1f}%"
            )

    # ── Recommendations ───────────────────────────────────────────────────────

    print(f"\n{bar}")
    print("  Business Recommendations")
    print(bar)

    for idx, rec in enumerate(recommendations, start=1):
        # Wrap long recommendations at 56 characters to keep output readable
        # in standard 80-column terminals.
        words:   list[str] = rec.split()
        lines:   list[str] = []
        current: str       = ""

        for word in words:
            if len(current) + len(word) + 1 > 56:
                lines.append(current)
                current = word
            else:
                current = f"{current} {word}".strip()
        if current:
            lines.append(current)

        prefix: str = f"  {idx}. "
        indent: str = " " * len(prefix)
        print(prefix + lines[0])
        for continuation in lines[1:]:
            print(indent + continuation)

    # ── Footer ────────────────────────────────────────────────────────────────

    print(f"\n{'=' * 60}\n")


