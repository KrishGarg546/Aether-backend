def calculate_campaign_metrics(receipts: pd.DataFrame) -> dict[str, Any]:
    """Calculate aggregate campaign performance metrics from receipt records.

    Computes delivery, engagement, and failure rates across all campaigns
    represented in the receipt log.  All rates conform to the metric
    definitions documented in the module docstring.

    Parameters
    ----------
    receipts:
        DataFrame of receipt records as returned by ``load_receipts()``.
        Must contain at minimum the ``communication_id`` and ``event``
        columns.  An empty DataFrame is accepted and produces zero-value
        metrics.

    Returns
    -------
    dict[str, Any]
        A flat dictionary with the following keys:

        ``total_communications`` (int)
            Unique communication IDs observed across all receipts.
        ``total_receipts`` (int)
            Total number of receipt rows in the input DataFrame.
        ``delivery_rate`` (float)
            Percentage of dispatched communications that were delivered.
        ``open_rate`` (float)
            Percentage of delivered communications that were opened or read.
        ``click_rate`` (float)
            Percentage of delivered communications that were clicked.
        ``failure_rate`` (float)
            Percentage of dispatched communications that failed.

    Notes
    -----
    - All rate values are expressed as percentages (0.0 – 100.0).
    - Division-by-zero is handled via ``_safe_rate``; zero denominators
      produce 0.0 rather than raising an exception.
    - ``open_rate`` combines both OPENED and READ events because different
      channels report engagement differently (EMAIL emits OPENED; SMS and
      WHATSAPP emit READ).
    """
    if receipts.empty:
        return {
            "total_communications": 0,
            "total_receipts":       0,
            "delivery_rate":        0.0,
            "open_rate":            0.0,
            "click_rate":           0.0,
            "failure_rate":         0.0,
        }

    event_counts: pd.Series = receipts["event"].value_counts()

    dispatched: int = int(event_counts.get("DISPATCHED", 0))
    delivered:  int = int(event_counts.get("DELIVERED",  0))
    failed:     int = int(event_counts.get("FAILED",     0))
    opened:     int = int(event_counts.get("OPENED",     0))
    read:       int = int(event_counts.get("READ",       0))
    clicked:    int = int(event_counts.get("CLICKED",    0))

    total_communications: int = receipts["communication_id"].nunique()
    total_receipts:       int = len(receipts)

    delivery_rate: float = _safe_rate(delivered, dispatched)
    open_rate:     float = _safe_rate(opened + read, delivered)
    click_rate:    float = _safe_rate(clicked, delivered)
    failure_rate:  float = _safe_rate(failed, dispatched)

    return {
        "total_communications": total_communications,
        "total_receipts":       total_receipts,
        "delivery_rate":        delivery_rate,
        "open_rate":            open_rate,
        "click_rate":           click_rate,
        "failure_rate":         failure_rate,
    }


