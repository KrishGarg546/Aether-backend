def generate_recommendations(metrics: dict[str, Any]) -> list[str]:
    """Generate deterministic business recommendations from campaign metrics.

    Applies a fixed rule set to the metric dictionary produced by
    ``calculate_campaign_metrics()``.  Every threshold and the
    recommendation text it triggers are locked in V1; changes require a
    corresponding ProjectDecision.md entry.

    At least one recommendation is always returned.  If no rule thresholds
    are breached and no positive signals are detected, a neutral baseline
    recommendation is appended as a fallback, guaranteeing a non-empty list.

    Parameters
    ----------
    metrics:
        Dictionary as returned by ``calculate_campaign_metrics()``.  Must
        contain ``delivery_rate``, ``open_rate``, ``click_rate``, and
        ``failure_rate`` keys with float values in the [0.0, 100.0] range.

    Returns
    -------
    list[str]
        An ordered list of human-readable recommendation strings, each
        describing one actionable business insight.  The list always
        contains at least one entry.

    Raises
    ------
    KeyError
        If any required metric key is absent from the input dictionary.

    Notes
    -----
    Recommendations are evaluated independently; multiple rules may fire
    for the same metrics snapshot.  The order reflects severity: critical
    alerts appear before improvement suggestions, which appear before
    positive signals.
    """
    delivery_rate: float = metrics["delivery_rate"]
    open_rate:     float = metrics["open_rate"]
    click_rate:    float = metrics["click_rate"]
    failure_rate:  float = metrics["failure_rate"]

    recommendations: list[str] = []

    # ── Critical alerts (highest priority) ──────────────────────────────────

    if failure_rate > _FAILURE_RATE_CRITICAL_THRESHOLD:
        recommendations.append(
            f"High failure rate detected ({failure_rate:.1f}%). "
            "Review channel strategy and provider reliability immediately."
        )

    if delivery_rate < _DELIVERY_RATE_CRITICAL_THRESHOLD:
        recommendations.append(
            f"Delivery rate is below threshold ({delivery_rate:.1f}% vs "
            f"{_DELIVERY_RATE_CRITICAL_THRESHOLD:.0f}% minimum). "
            "Investigate channel delivery issues and audience data quality."
        )

    # ── Elevated warnings ────────────────────────────────────────────────────

    if (
        _FAILURE_RATE_ELEVATED_THRESHOLD < failure_rate
        <= _FAILURE_RATE_CRITICAL_THRESHOLD
    ):
        recommendations.append(
            f"Elevated failure rate observed ({failure_rate:.1f}%). "
            "Monitor channel health closely and review suppression lists."
        )

    # ── Improvement suggestions ──────────────────────────────────────────────

    if open_rate < _OPEN_RATE_LOW_THRESHOLD:
        recommendations.append(
            f"Open rate is low ({open_rate:.1f}% vs "
            f"{_OPEN_RATE_LOW_THRESHOLD:.0f}% benchmark). "
            "Consider revising subject lines, send times, or message preview text."
        )

    if click_rate < _CLICK_RATE_LOW_THRESHOLD:
        recommendations.append(
            f"Click-through rate is below benchmark ({click_rate:.1f}% vs "
            f"{_CLICK_RATE_LOW_THRESHOLD:.0f}% target). "
            "Review call-to-action copy, offer clarity, and landing page relevance."
        )

    # ── Positive signals ─────────────────────────────────────────────────────

    if delivery_rate >= _DELIVERY_RATE_HEALTHY_THRESHOLD:
        recommendations.append(
            f"Delivery performance is healthy ({delivery_rate:.1f}%). "
            "Channel infrastructure and audience data quality are sound."
        )

    if open_rate >= _OPEN_RATE_HIGH_THRESHOLD:
        recommendations.append(
            f"Audience engagement is strong ({open_rate:.1f}% open rate). "
            "Message content is resonating with this segment."
        )

    if click_rate > _CLICK_RATE_HIGH_THRESHOLD:
        recommendations.append(
            f"Campaign engagement is exceptionally strong ({click_rate:.1f}% "
            "click rate). Consider scaling budget, audience, or send frequency."
        )

    # ── Fallback (guarantee at least one recommendation) ─────────────────────

    if not recommendations:
        recommendations.append(
            "Campaign metrics are within normal operating ranges. "
            "Continue monitoring delivery and engagement trends across segments."
        )

    return recommendations


